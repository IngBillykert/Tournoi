    <h1>Manuel D'utilisation pour le tournoi</h1>
    </center><br>

    <center>
        <p>
            Ce Programme est la pour l'avancement d'un tournoi, des que vous etes sur le page acceuil 
            vous allez voir les equipes qui sont en tete de serie, apres vous pouvez cliquez sur 
            la lettre <strong>Tirage</strong> pour faire un tirage aleatoire. Ce boutton va vous dirigez 
            sur une page ou il aura les phases de poules et les confrontations du premier tour du tournoi en question.
            Vous allez entrer les scores manuellement dans les parties vides des scores dans la partie confrontation.

            <br> <br>
            Apres les entrer vous alles voir un boutton de <strong>classement</strong> cliques sur le boutton pour vous dirigez vers 
            la page ou il y aura les classemnt des equipes. Apres vous allez voir un lien en bas de la pages
            qui est <strong>Phase Final</strong>.
            
            <br> <br>
            Cliques sur le lien et il vous affichera les matchs du demi Final.
            Vous allez encore rentrer les scores dans la partie Score du tableau, apres la saisie des scores,
            pressez le boutton <strong>Valider</strong> il vous emmennera sur la page qui a les infos sur
            les equipes qui seront en petit Final(Les equipe qui ont perdu en demi final). 
            Et la vous allez rentrer les scores du match du peti final pour 
            determiner l'equipe qui est 3eme du championat. Vous allez cliques encore sur le boutton <strong>Valider</strong>
            pour voir l'equipe qui est 3eme et l'equipe qui est quatrieme du championat.

            <br> <br>
            Et ensuite un boutton entutiler <strong>Final</strong> apparaitra cliquez sur lui de nouveau, ce 
            boutton vous emmennera sur la page final qui aura les deux(2) equipes qui avait gagne en demi final.
            Vous allez encore rentrer les scores dans la partie Score du tableau, apres la saisie des scores,
            pressez le boutton <strong>Valider</strong> et il vous affichera l'equi qui est 2eme du championat
            et aussi Le vainqueur du championat.
    
        </p>
